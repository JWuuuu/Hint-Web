interface LanguageContextValue {
  language: HintLanguage;
  languages: typeof LANGUAGE_OPTIONS;
  setLanguage: (language: HintLanguage) => void;
  toggleLanguage: () => void;
  t: (key: string) => string;
}

const LanguageContext = createContext<LanguageContextValue | null>(null);

function getInitialLanguage(): HintLanguage {
  if (typeof window === "undefined") return "en";
  const stored = window.localStorage.getItem(LANGUAGE_STORAGE_KEY);
  return KNOWN_LANGUAGE_CODES.has(stored as HintLanguage) ? (stored as HintLanguage) : "en";
}

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [language, setLanguage] = useState<HintLanguage>(getInitialLanguage);

  useEffect(() => {
    const option = LANGUAGE_OPTIONS.find((item) => item.code === language) ?? LANGUAGE_OPTIONS[0]!;
    document.documentElement.lang = option.htmlLang;
    try {
      window.localStorage.setItem(LANGUAGE_STORAGE_KEY, language);
    } catch {
      // Local storage can be unavailable in private browsing.
    }
  }, [language]);

  const t = useCallback(
    (key: string) => SUPPLEMENTAL_COPY[language]?.[key] ?? COPY[language][key] ?? COPY.en[key] ?? key,
    [language],
  );

  const value = useMemo(
    () => ({
      language,
      languages: LANGUAGE_OPTIONS,
      setLanguage,
      toggleLanguage: () =>
        setLanguage((current) => {
          const index = LANGUAGE_OPTIONS.findIndex((item) => item.code === current);
          return LANGUAGE_OPTIONS[(index + 1) % LANGUAGE_OPTIONS.length]!.code;
        }),
      t,
    }),
    [language, t],
  );

  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>;
}

export function useLanguage() {
  const value = useContext(LanguageContext);
  if (!value) throw new Error("useLanguage must be used inside LanguageProvider");
  return value;
}
